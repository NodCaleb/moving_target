void Read_Joystick(void){
  
  // SendUint32AsDecimalWithNewline(&huart1, joystick_x);
  // SendUint32AsDecimalWithNewline(&huart1, joystick_y);

  //Top = 1
  if (joystick_y <= THRESHOLD_MOVEMENT_LOWER && joystick_x < THRESHOLD_MOVEMENT_UPPER && joystick_x > THRESHOLD_MOVEMENT_LOWER){
    data = 0x01;
  }
  //Top-right = 2
  else if (joystick_y <= THRESHOLD_MOVEMENT_LOWER && joystick_x >= THRESHOLD_MOVEMENT_UPPER){
    data = 0x02;
  }
  //Right = 3
  else if (joystick_x >= THRESHOLD_MOVEMENT_UPPER && joystick_y < THRESHOLD_MOVEMENT_UPPER && joystick_y > THRESHOLD_MOVEMENT_LOWER){
    data = 0x03;
  }
  //Bottom-right = 4
  else if (joystick_y >= THRESHOLD_MOVEMENT_UPPER && joystick_x >= THRESHOLD_MOVEMENT_UPPER){
    data = 0x04;
  }
  //Bottom = 5
  else if (joystick_y >= THRESHOLD_MOVEMENT_UPPER && joystick_x < THRESHOLD_MOVEMENT_UPPER && joystick_x > THRESHOLD_MOVEMENT_LOWER){
    data = 0x05;
  }
  //Bottom-left = 6
  else if (joystick_y >= THRESHOLD_MOVEMENT_UPPER && joystick_x <= THRESHOLD_MOVEMENT_LOWER){
    data = 0x06;
  }
  //Left = 7
  else if (joystick_x <= THRESHOLD_MOVEMENT_LOWER && joystick_y < THRESHOLD_MOVEMENT_UPPER && joystick_y > THRESHOLD_MOVEMENT_LOWER){
    data = 0x07;
  }
  //Top-left = 8
  else if (joystick_y <= THRESHOLD_MOVEMENT_LOWER && joystick_x <= THRESHOLD_MOVEMENT_LOWER){
    data = 0x08;
  }
  //Stop
  else{
	  data = 0x00;
  }

  if (data > 0 && (joystick_x >= THRESHOLD_HIGH_SPEED_UPPER || joystick_x <= THRESHOLD_HIGH_SPEED_LOWER || joystick_y >= THRESHOLD_HIGH_SPEED_UPPER || joystick_y <= THRESHOLD_HIGH_SPEED_LOWER)){
    data = data | 0x10;
  }

  USART1->DR = data;

}

void SendUint32AsDecimalWithNewline(UART_HandleTypeDef *huart, uint32_t value) {
    char buffer[16]; // Buffer large enough for a 32-bit decimal string and newline
    
    // Convert uint32_t to string as a decimal and add newline
    snprintf(buffer, sizeof(buffer), "%lu\r\n", (unsigned long)value);
    
    // Transmit the string via UART
    HAL_UART_Transmit(huart, (uint8_t *)buffer, strlen(buffer), HAL_MAX_DELAY);
}

